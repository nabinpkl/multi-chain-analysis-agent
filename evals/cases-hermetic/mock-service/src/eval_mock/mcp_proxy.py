"""FastMCP `/mcp` proxy for codex hermetic evals.

Codex's `CodexHttpMcpServer` connects to `${DATA_PLANE_URL}/mcp` and
expects the Streamable-HTTP MCP transport (per Rust's
`StreamableHttpService` at [backend/src/api/mod.rs](backend/src/api/mod.rs)).
In hermetic mode `DATA_PLANE_URL=http://eval-mock:8005`, so codex
hits this module.

Schema source of truth: the tool descriptors served on `tools/list`
come from `schemas.json`, a build-time snapshot generated by
`cargo run --bin dump-mcp-schemas` (see
[backend/src/bin/dump_mcp_schemas.rs](../../../backend/src/bin/dump_mcp_schemas.rs)).
A Rust drift test fails any build where the live `McaeMcp::schemas()`
output differs from the checked-in JSON, so codex always sees the
exact upstream-derived schemas without any hand-typed Python copies.

Why `mcp.server.fastmcp.FastMCP` not jlowin's `fastmcp`: FastMCP's
high-level `@mcp.tool` decorator derives schemas from Python type
hints, which would force us to hand-type the same shapes the Rust
MCP already declares. By using the mcp SDK's underlying low-level
`Server` (exposed as `mcp._mcp_server`) we install our own
`list_tools` / `call_tool` handlers that read schemas directly from
the snapshot dict. Transport plumbing (`streamable_http_app`,
`session_manager.run()` for lifespan) comes for free from the
FastMCP wrapper.

Tool dispatch reads from the shared `FixtureStore` in
[fixtures.py](fixtures.py) (same store the HTTP shim reads from,
populated by `POST /eval/setup` from the eval runner before each
case). `emit_claims` pushes onto the per-snapshot claim queue so the
SSE drain at `GET /turn/{snapshot_id}/claims` in
[http_shim.py](http_shim.py) replays them to the agent-service loop
driver, matching the Rust side's per-snapshot mpsc channel shape at
[backend/src/mcp.rs](../../../backend/src/mcp.rs)::emit_claims.
"""

from __future__ import annotations

import json
import os
from pathlib import Path
from typing import Any

import mcp.types as mcp_types
import structlog
from mcp.server.fastmcp import FastMCP
from mcp.server.transport_security import TransportSecuritySettings

from eval_mock.fixtures import STORE

log = structlog.get_logger(__name__)

# `schemas.json` lives at the mock-service package root, two levels
# above this file (`src/eval_mock/mcp_proxy.py` → `src/` → root).
SCHEMAS_PATH = Path(__file__).resolve().parent.parent.parent / "schemas.json"

# ---------------------------------------------------------------------------
# Per-turn tool-call budget. Mirrors the Python side at
# `agent_service/policy/resource_bounds.py` and the Rust side at
# `backend/src/mcp.rs::try_consume_budget` byte-for-byte. We duplicate
# the constants here (rather than depending on agent-service) because
# the mock-service is intentionally a standalone uv package; the unit
# tests in `test_policy_resource_bounds.py` pin the Python side and
# `backend/src/mcp.rs::tests` pins the Rust side, so drift between any
# two of the three surfaces shows up immediately. Both runtimes use the
# same env var so the operator tunes one knob and all three surfaces
# move together.
# ---------------------------------------------------------------------------

TURN_TOOL_CALL_BUDGET: int = int(os.environ.get("AGENT_TURN_TOOL_CALL_BUDGET", "8"))

# Sentinel error-kind. The codex driver greps the TOOL_COMPLETED
# event's output for this exact substring to stamp
# `mcae.turn.budget_exhausted`. Drift breaks the OTel signal
# silently; keep in lockstep with
# `agent_service.policy.resource_bounds.NO_MORE_LOOKUPS_ERROR_KIND`
# and the Rust `NO_MORE_LOOKUPS_ERROR_KIND` constant.
NO_MORE_LOOKUPS_ERROR_KIND: str = "no_more_lookups_this_turn"

NO_MORE_LOOKUPS_PAYLOAD: dict[str, str] = {
    "error": NO_MORE_LOOKUPS_ERROR_KIND,
    "guidance": (
        "You have used this turn's tool budget. Do not call any "
        "more lookup tools. Finalize the answer using only the "
        "data already returned this turn. If the data so far is "
        "not enough, say so honestly in the narrative."
    ),
}

# Only the three read-side primitives count against the budget.
# `emit_claims` is reporting, not lookup. Mirrors the Python
# `BUDGETED_PRIMITIVES` and the Rust enforcement set.
_BUDGETED_TOOLS = frozenset(
    {"wallet_profile", "community_summary", "get_token_info"}
)


def load_schemas() -> list[dict[str, Any]]:
    """Read the build-time MCP schema snapshot. Returns the list of
    Tool descriptors `cargo run --bin dump-mcp-schemas` emits
    (`name`, `description`, `inputSchema` keys per entry)."""
    if not SCHEMAS_PATH.exists():
        raise RuntimeError(
            f"MCP schemas snapshot missing at {SCHEMAS_PATH}. "
            "Run `just dump-mcp-schemas` to regenerate from the live "
            "Rust MCP tool router before starting the mock service."
        )
    return json.loads(SCHEMAS_PATH.read_text())


# ---------------------------------------------------------------------------
# Canonical-mint stamp. Mirrors backend/src/canonical_mints.rs::REGISTRY.
# Imported from http_shim so there's exactly one copy in this process.
# ---------------------------------------------------------------------------

from eval_mock.http_shim import _stamp_verification  # noqa: E402


# ---------------------------------------------------------------------------
# Tool handlers. Each consults the shared `STORE`. Return values are
# JSON-shaped dicts wrapped in TextContent so codex sees the same
# `{"value": ..., "provenance": [...]}` envelope the Rust MCP emits at
# `backend/src/mcp.rs` (see `McpEnvelope` there).
# ---------------------------------------------------------------------------


def _handle_wallet_profile(args: dict[str, Any]) -> dict[str, Any]:
    """Mirror of `wallet_profile` in Rust. Returns the
    `{value, provenance}` envelope; the eval runner doesn't model
    `subgraph_slice` so the field is omitted."""
    addr = args.get("addr", "")
    fixture = STORE.wallet_profile.get(addr)
    if fixture is not None:
        value = fixture.payload
    else:
        # Empty-shape default (mirrors http_shim default for unknown wallets).
        value = {
            "addr": addr,
            "role": "NODE_ROLE_UNKNOWN",
            "community_id": 0,
            "stats": {
                "degree": 0,
                "total_volume_lamports": 0.0,
                "in_volume_lamports": 0.0,
                "out_volume_lamports": 0.0,
                "bidir_volume_lamports": 0.0,
                "sol_degree": 0,
                "spl_degree": 0,
            },
            "top_counterparties": [],
            "age_in_window_secs": 0,
        }
    return {
        "value": value,
        "provenance": [{"kind": "wallet", "addr": addr, "idx": 0}],
    }


def _handle_community_summary(args: dict[str, Any]) -> dict[str, Any]:
    community_id = int(args.get("community_id", 0))
    fixture = STORE.community_summary.get(community_id)
    if fixture is not None:
        value = fixture.payload
    else:
        value = {
            "community_id": community_id,
            "size": 0,
            "total_volume": 0.0,
            "internal_volume": 0.0,
            "external_volume": 0.0,
            "edge_count": 0,
            "top_wallets": [],
        }
    return {
        "value": value,
        "provenance": [{"kind": "community", "id": community_id}],
    }


def _handle_get_token_info(args: dict[str, Any]) -> dict[str, Any]:
    """Mirror of `get_token_info`. Stamps `verified` / `canonical_*`
    via the local mirror of `canonical_mints::stamp_verification`.
    Unknown mints land with `verified=false` + empty metadata, same
    as the live route's `not_found` shape."""
    mint = str(args.get("mint", "")).strip()
    if not mint:
        raise ValueError("mint must be non-empty")

    fixture = STORE.get_token_info.get(mint)
    if fixture is None:
        payload = {
            "mint": mint,
            "name": None,
            "symbol": None,
            "uri": None,
            "update_authority": None,
            "source_program": "",
        }
    else:
        payload = {
            "mint": fixture.mint,
            "name": fixture.name,
            "symbol": fixture.symbol,
            "uri": fixture.uri,
            "update_authority": fixture.update_authority,
            "source_program": fixture.source_program,
        }
    _stamp_verification(payload)
    return payload


def _handle_emit_claims(args: dict[str, Any]) -> dict[str, Any]:
    """Mirror of `emit_claims`. Pushes each parsed claim onto the
    per-snapshot queue in `STORE.claim_queues`; the SSE drain at
    `/turn/{snapshot_id}/claims` consumes from that queue and replays
    to the agent-service loop driver. Matches the Rust side's
    batched-emit shape at `backend/src/mcp.rs`."""
    snapshot_id = str(args.get("snapshot_id", ""))
    if not snapshot_id:
        raise ValueError("snapshot_id is required")

    raw_claims = args.get("claims", [])
    if not isinstance(raw_claims, list):
        raise ValueError("claims must be a list")

    n_pushed = 0
    for claim in raw_claims:
        if not isinstance(claim, dict):
            raise ValueError(
                f"each claim must be an object; got {type(claim).__name__}"
            )
        STORE.push_claim(snapshot_id, claim)
        n_pushed += 1

    return {"emitted": n_pushed, "snapshot_id": snapshot_id}


_DISPATCH = {
    "wallet_profile": _handle_wallet_profile,
    "community_summary": _handle_community_summary,
    "get_token_info": _handle_get_token_info,
    "emit_claims": _handle_emit_claims,
}

# Read-side tools that return untrusted on-chain text and therefore
# wrap their payload in `<external_data primitive="…">`. Mirrors the
# Rust set in `backend/src/mcp.rs::tool_result_external_data` call
# sites. `emit_claims` is omitted because it returns an ack of fields
# the agent itself authored, not external data.
_EXTERNAL_DATA_TOOLS = frozenset({
    "wallet_profile",
    "community_summary",
    "get_token_info",
})


def wrap_external_data(primitive: str, payload: Any) -> str:
    """Build the `<external_data primitive="…">` envelope string the
    model sees in its conversation. Mirrors
    `agent_service.boundary.wrap_external_data` byte-for-byte (compact
    JSON, newline-padded) and the Rust side at
    `backend/src/mcp.rs::wrap_external_data`. Exposed as a module-
    level function so tests can pin the shape without booting the
    full MCP transport.

    Wire-layer defense: every `<` and `>` inside the JSON body is
    unicode-escaped to `\\u003c` / `\\u003e`. That guarantees the
    only literal `</external_data>` substring in the emitted string
    is the real envelope close, even when an attacker plants the
    close tag inside a wrapped field. Mirrors the same defense in
    the Python boundary and Rust MCP wrappers so all three wires
    stay byte-for-byte aligned."""
    body = json.dumps(payload, separators=(",", ":"))
    body = body.replace("<", "\\u003c").replace(">", "\\u003e")
    return f'<external_data primitive="{primitive}">\n{body}\n</external_data>'


# ---------------------------------------------------------------------------
# FastMCP wrapper. We use the SDK's `FastMCP` for transport plumbing
# (streamable_http_app, session_manager) but install our own
# `list_tools` / `call_tool` handlers on the underlying low-level
# `Server` so schemas come from `schemas.json` rather than Python
# type hints. The `_mcp_server` attribute is documented enough across
# the mcp SDK examples to lean on; if a future SDK rename touches it,
# the snapshot drift test stays the canary.
# ---------------------------------------------------------------------------


def _build_mcp() -> FastMCP:
    # mcp SDK's StreamableHTTPServerTransport ships a DNS-rebinding
    # defense that rejects requests whose `Host:` header isn't on an
    # allowlist (default: `127.0.0.1`, `localhost`). In docker compose
    # the agent-service-eval sibling reaches us at
    # `Host: eval-mock:8005`, which the default allowlist rejects with
    # `Invalid Host header` and the codex handshake then 421s.
    #
    # Mirror the same posture as Rust at
    # [backend/src/api/mod.rs](backend/src/api/mod.rs)::internal_router:
    # accept loopback + the docker service name. `MCP_ALLOWED_HOSTS`
    # env follows the comma-separated shape Rust uses so a developer
    # can extend it without a rebuild.
    # `TransportSecuritySettings._validate_host` does exact-match
    # plus a `<host>:*` wildcard-port pattern. Docker compose hops in
    # use `Host: eval-mock:8005`, so entries here include the wildcard
    # port form to cover any port the dev or eval profile picks.
    allowed_hosts = [
        h.strip()
        for h in os.environ.get(
            "MCP_ALLOWED_HOSTS",
            "127.0.0.1,127.0.0.1:*,localhost,localhost:*,eval-mock,eval-mock:*",
        ).split(",")
        if h.strip()
    ]
    mcp = FastMCP(
        "eval-mock",
        stateless_http=True,
        transport_security=TransportSecuritySettings(
            allowed_hosts=allowed_hosts,
        ),
    )
    # FastMCP defaults `streamable_http_path` to `/mcp`. Since we mount
    # the resulting ASGI app under `/mcp` on the main FastAPI app,
    # leaving the default would yield `/mcp/mcp` on the wire. Setting
    # the inner path to `/` lets the mount handle the prefix cleanly.
    mcp.settings.streamable_http_path = "/"
    server = mcp._mcp_server  # underlying mcp.server.lowlevel.Server

    schemas = load_schemas()

    @server.list_tools()
    async def list_tools() -> list[mcp_types.Tool]:
        return [
            mcp_types.Tool(
                name=entry["name"],
                description=entry.get("description", ""),
                inputSchema=entry["inputSchema"],
            )
            for entry in schemas
        ]

    @server.call_tool()
    async def call_tool(
        name: str, arguments: dict[str, Any]
    ) -> tuple[list[mcp_types.TextContent], dict[str, Any]] | list[mcp_types.TextContent]:
        handler = _DISPATCH.get(name)
        if handler is None:
            raise ValueError(f"unknown tool: {name}")
        # Per-turn budget gate. For the three read-side primitives,
        # increment the per-snapshot counter and short-circuit with
        # the no_more_lookups envelope when the cap is reached. Same
        # contract Rust enforces in `try_consume_budget` so the
        # codex hermetic path exercises the budget exactly as
        # production does. `emit_claims` is exempt (reporting, not
        # a lookup). Missing snapshot_id => skip the gate (caller
        # bypassed /turn/begin); mirrors Rust's behavior.
        if name in _BUDGETED_TOOLS:
            snapshot_id = str(arguments.get("snapshot_id", "") or "").strip()
            if snapshot_id and not STORE.try_consume_budget(
                snapshot_id, TURN_TOOL_CALL_BUDGET
            ):
                envelope = wrap_external_data(name, NO_MORE_LOOKUPS_PAYLOAD)
                return (
                    [mcp_types.TextContent(type="text", text=envelope)],
                    dict(NO_MORE_LOOKUPS_PAYLOAD),
                )
        try:
            result = handler(arguments)
        except Exception as e:  # noqa: BLE001
            log.warning(
                "mcp_tool_call_failed",
                tool=name,
                error=str(e),
            )
            raise
        # Match the Rust side's `CallToolResult` shape exactly. Read-
        # side tools (`wallet_profile`, `community_summary`,
        # `get_token_info`) wrap the model-visible text payload in the
        # `<external_data primitive="…">` envelope so the system
        # prompt's instruction-rejection rule applies to attacker-
        # controlled strings (token name/symbol/uri, wallet tag)  the
        # same defense `wrap_external_data` provides on the
        # pydantic-ai HTTP path. We also surface the raw value as
        # `structuredContent` so the Python codex_driver's
        # `_extract_mcp_envelope` keeps populating the per-thread
        # binding store; the mcp SDK's lowlevel `call_tool` accepts a
        # `(unstructured, structured)` tuple precisely for this split.
        # Drift caught upstream by
        # `backend/src/mcp.rs::tests::tool_result_external_data_text_
        # carries_envelope`. `emit_claims` is a write-side ack with no
        # untrusted text, so it returns bare JSON only.
        if name in _EXTERNAL_DATA_TOOLS:
            envelope = wrap_external_data(name, result)
            return (
                [mcp_types.TextContent(type="text", text=envelope)],
                result,
            )
        return [
            mcp_types.TextContent(
                type="text",
                text=json.dumps(result, separators=(",", ":")),
            )
        ]

    log.info("mcp_proxy_ready", tool_count=len(schemas))
    return mcp


# Module-level singleton. `eval_mock.main` references `MCP` to wire
# both the ASGI app (`streamable_http_app`) and the lifespan
# context (`session_manager.run()`).
MCP = _build_mcp()


def get_app():
    """Return the Streamable-HTTP ASGI app for mounting at `/mcp` on
    the main FastAPI instance. Stateless transport so each codex
    request stands alone (the hermetic runner is single-shot per
    case)."""
    return MCP.streamable_http_app()


def get_session_manager():
    """Expose the session manager so `main.py` can run it inside the
    FastAPI lifespan. Without this, the transport never accepts
    incoming sessions and all `/mcp` POSTs return 503."""
    return MCP.session_manager
