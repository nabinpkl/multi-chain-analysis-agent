"""Regression baselines for eval suites.

A baseline is the per-probe pass/fail map for a suite, snapshotted
to disk and committed under `evals/baselines/<suite_stem>.json`.
After every `just eval` run the runner diffs the run's results
against the matching baseline; any flip in either direction (pass
to fail, fail to pass) plus any schema delta (probe added or
removed) fails the run loudly. `just eval-baseline <suite>`
overwrites the baseline from the latest run.

Why pass/fail and not numeric drift:

The whole contract is "this probe holds against this trace shape".
Probes that assert numeric bounds (e.g. `span_latency_p50_under`)
encode the bound in the probe spec; the baseline tracks whether
the probe passed against its bound, not the underlying number. So
day-to-day variance in latency that stays under the bound never
trips a regression alarm; a regression that pushes p50 over the
bound trips exactly one (the latency probe). This is the same
shape unit-test suites use: pass/fail per case, no per-step
intermediate-state diffing.

Why JSON and not YAML:

Baselines are read by tooling and written by `eval-baseline`, not
hand-edited. JSON's stable formatting (one line per (case, probe))
makes git diffs readable; YAML's `pass`/`fail` quoting quirks add
noise without buying anything. Cases (which ARE hand-edited) stay
YAML.

Why per-suite and not per-run:

A baseline is a contract on the suite's expected output, not on a
specific run that produced it. Provenance (`captured_at`,
`git_sha`, `agent_version`) records which run minted the contract;
the contract itself is the pass/fail map.
"""

from __future__ import annotations

from datetime import datetime, timezone
from pathlib import Path
from typing import Literal

from pydantic import BaseModel, ConfigDict, Field

from agent_service.evals.schema import ProbeResult

ProbeOutcome = Literal["pass", "fail"]


class Baseline(BaseModel):
    """Committed regression contract for one suite. Loaded from
    `evals/baselines/<suite_stem>.json` before each run, diffed
    against the run's results, regenerated by `eval-baseline`.

    `results` is a nested dict: `case_id -> probe_id -> outcome`.
    Stable JSON shape (sorted keys, one line per (case, probe) once
    re-serialized) makes git diffs easy to read.
    """

    model_config = ConfigDict(extra="forbid")

    suite: str
    captured_at: datetime
    git_sha: str
    agent_version: str
    results: dict[str, dict[str, ProbeOutcome]]


class RegressionReport(BaseModel):
    """Output of `diff_against_baseline`. Three orthogonal delta
    sets; any non-empty set fails the run.

    `new_failures` is the regression-net's reason to exist: probes
    that were locked in as passing and now fail. Always investigate.

    `newly_passing` is the inverse: probes locked in as failing
    that now pass. Could be a real fix the operator wants to commit
    via `eval-baseline`, or a flaky pass that will revert. Never
    swallow silently; the operator has to acknowledge with an
    explicit baseline update.

    `schema_deltas` covers (case, probe) pairs that exist in only
    one side: a probe added to a YAML case without a baseline bump,
    a probe removed without a baseline bump, a whole case added or
    removed. Same disposition as `newly_passing`: not a regression
    in the strict sense, but unacknowledged drift that hides real
    regressions if left unaddressed.
    """

    model_config = ConfigDict(extra="forbid")

    suite: str
    new_failures: list[tuple[str, str]] = Field(default_factory=list)
    newly_passing: list[tuple[str, str]] = Field(default_factory=list)
    schema_deltas: list[tuple[str, str, str]] = Field(default_factory=list)
    inconclusive: list[tuple[str, str, str]] = Field(default_factory=list)
    # schema_deltas tuple shape: (case_id, probe_id, "added"|"removed")
    # inconclusive tuple shape:  (case_id, probe_id, error_summary)
    # Inconclusive entries are NOT a regression event; they are
    # surfaced so the operator knows which probes were suppressed
    # and can re-run if the suppression looks wrong (e.g. the trace
    # shows a non-transient terminal failure that infra_health
    # mis-classified).

    @property
    def is_clean(self) -> bool:
        """Inconclusive entries are NOT counted toward `is_clean`.
        A run with all-passing-or-inconclusive results is clean,
        because no probe disagreed with its baseline. The operator
        sees the inconclusive list separately and decides whether
        to re-run."""
        return not (self.new_failures or self.newly_passing or self.schema_deltas)


def load_baseline(path: Path) -> Baseline | None:
    """Read a baseline file. Returns None if the file does not
    exist (the first-run case for a brand new suite, where the
    runner will print a hint to mint one via `eval-baseline`)."""
    if not path.exists():
        return None
    return Baseline.model_validate_json(path.read_text())


def _walk_run_results(
    run_root: Path,
) -> tuple[dict[str, dict[str, ProbeOutcome]], list[tuple[str, str, str]]]:
    """Walk `<run_root>/<case>/<probe>.json` and split into:
      - the pass/fail map for the diff (excludes inconclusive)
      - the inconclusive list, with each entry `(case_id, probe_id,
        error_summary)` for surfacing in the regression report.

    Inconclusive results are excluded from the pass/fail map by
    design: their outcome was suppressed by an infra-health check,
    so comparing them against the baseline would either flip a
    false regression (if baseline=pass and we now report fail) or
    silently drop the data (if treated as a missing key). The
    inconclusive list keeps them visible to the operator.

    Skips `run.json` (the RunMetadata summary file sits at
    run_root, not under a case dir).
    """
    results: dict[str, dict[str, ProbeOutcome]] = {}
    inconclusive: list[tuple[str, str, str]] = []
    for case_dir in run_root.iterdir():
        if not case_dir.is_dir():
            continue
        case_map: dict[str, ProbeOutcome] = {}
        for probe_file in case_dir.glob("*.json"):
            r = ProbeResult.model_validate_json(probe_file.read_text())
            if r.inconclusive:
                inconclusive.append((r.case_id, r.probe_id, r.error or ""))
                continue
            case_map[r.probe_id] = "pass" if r.passed else "fail"
        if case_map:
            results[case_dir.name] = case_map
    return results, inconclusive


def build_baseline_from_run(
    *,
    suite: str,
    run_root: Path,
    git_sha: str,
    agent_version: str,
) -> Baseline:
    """Construct a baseline from the artifacts of a completed run.
    The walk is the same one `summarize_run` does; we keep them
    separate because RunMetadata persists eagerly during the run
    and would force a circular-dependency on baselines.

    Inconclusive entries from `_walk_run_results` are intentionally
    discarded here: a baseline records the contract, and an
    inconclusive run has no contract for those probes. The
    `update_baseline` CLI's `_has_failures` check refuses to mint a
    baseline from a run with any inconclusive probes (without
    --force), so by the time we reach this function the inconclusive
    list is expected to be empty in normal use."""
    results, _inconclusive = _walk_run_results(run_root)
    return Baseline(
        suite=suite,
        captured_at=datetime.now(timezone.utc),
        git_sha=git_sha,
        agent_version=agent_version,
        results=results,
    )


def diff_against_baseline(baseline: Baseline, run_root: Path) -> RegressionReport:
    """Pure pass/fail diff of a completed run against the committed
    baseline. Returns a RegressionReport with three orthogonal
    delta sets plus an inconclusive list. Numeric `observed`
    fields and `score` are intentionally not part of the diff;
    see module docstring.

    Inconclusive probes are excluded from the pass/fail diff
    entirely. They are NOT counted as schema deltas (because the
    probe IS in the YAML, just suppressed for this run); they are
    surfaced in `report.inconclusive` so the operator sees them.
    A baseline that contained pass for a probe that ran
    inconclusive in this run produces neither a `new_failures` nor
    a `newly_passing` entry: the diff has no signal to compare.
    """
    current, inconclusive = _walk_run_results(run_root)

    new_failures: list[tuple[str, str]] = []
    newly_passing: list[tuple[str, str]] = []
    schema_deltas: list[tuple[str, str, str]] = []

    inconclusive_keys = {(c, p) for (c, p, _) in inconclusive}
    baseline_keys = {
        (case, probe)
        for case, probes in baseline.results.items()
        for probe in probes
    }
    current_keys = {
        (case, probe)
        for case, probes in current.items()
        for probe in probes
    }

    for case, probe in sorted(baseline_keys & current_keys):
        b = baseline.results[case][probe]
        c = current[case][probe]
        if b == c:
            continue
        if b == "pass" and c == "fail":
            new_failures.append((case, probe))
        else:
            newly_passing.append((case, probe))

    # A probe that is inconclusive this run but exists in the
    # baseline is NOT a schema delta; subtract those out before
    # computing added/removed.
    for case, probe in sorted(current_keys - baseline_keys):
        schema_deltas.append((case, probe, "added"))
    for case, probe in sorted(baseline_keys - current_keys - inconclusive_keys):
        schema_deltas.append((case, probe, "removed"))

    return RegressionReport(
        suite=baseline.suite,
        new_failures=new_failures,
        newly_passing=newly_passing,
        schema_deltas=schema_deltas,
        inconclusive=inconclusive,
    )


def persist_baseline(baseline: Baseline, path: Path) -> None:
    """Write a baseline file. Sorts the inner dicts for stable git
    diffs across `eval-baseline` re-runs."""
    sorted_results = {
        case: dict(sorted(probes.items()))
        for case, probes in sorted(baseline.results.items())
    }
    out = baseline.model_copy(update={"results": sorted_results})
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(out.model_dump_json(indent=2) + "\n")


def render_report(report: RegressionReport) -> str:
    """Plain-text report renderer for the CLI. Returns the empty
    string only when the diff is clean AND there are no
    inconclusive probes (clean + inconclusive prints the
    inconclusive list so the operator decides whether to re-run)."""
    if report.is_clean and not report.inconclusive:
        return ""
    lines: list[str] = [f"regression report for suite '{report.suite}':"]
    if report.new_failures:
        lines.append("  new failures (probe was passing, now fails):")
        for case, probe in report.new_failures:
            lines.append(f"    FAIL  {case} / {probe}")
    if report.newly_passing:
        lines.append("  newly passing (was failing, now passes; bump baseline if intended):")
        for case, probe in report.newly_passing:
            lines.append(f"    PASS  {case} / {probe}")
    if report.schema_deltas:
        lines.append("  schema deltas (probe added or removed without baseline bump):")
        for case, probe, kind in report.schema_deltas:
            lines.append(f"    {kind.upper():<7} {case} / {probe}")
    if report.inconclusive:
        lines.append(
            "  inconclusive (suppressed by infra-health; not a regression, "
            "re-run if surprising):"
        )
        for case, probe, summary in report.inconclusive:
            lines.append(f"    SKIP  {case} / {probe}   {summary}")
    return "\n".join(lines)
